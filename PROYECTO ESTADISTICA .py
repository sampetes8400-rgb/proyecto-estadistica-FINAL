    plt.figure(figsize=(10, 5))
    plt.fill_between(x, y, color="orange", alpha=0.5)
    plt.plot(x, y, color="darkorange", marker="o")
    plt.title(f"Gráfica de área - {titulo}")
    plt.xlabel("Categoría / Valor")
    plt.ylabel("Frecuencia")
