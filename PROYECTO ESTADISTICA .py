def main():
    excel_bytes, nombre_archivo = subir_excel()
    libro = cargar_libro(excel_bytes)

    hojas_utiles = obtener_hojas_utiles(libro)
    datos_preparados = {}

    for hoja in hojas_utiles:
        datos_preparados[hoja] = preparar_hoja(hoja, libro[hoja])

    print("=" * 70)
    print("PROYECTO LISTO")
    print("=" * 70)
    print(f"Archivo analizado: {nombre_archivo}")

    while True:
        mostrar_menu_principal()
        opcion = input("\nElige una opción: ").strip()

        if opcion == "1":
            print("\nHojas disponibles:")
            for h in datos_preparados.keys():
                print("-", h)

        elif opcion == "2":
            hoja = elegir_hoja(datos_preparados)
            if hoja:
                tabla = datos_preparados[hoja]
                if tabla is not None:
                    mostrar_datos(tabla)
                else:
                    print("No se pudo preparar esa hoja.")

        elif opcion == "3":
            hoja = elegir_hoja(datos_preparados)
            if hoja:
                tabla = datos_preparados[hoja]
                if tabla is not None:
                    mostrar_resumen_hoja(hoja, tabla)
                else:
                    print("No se pudo preparar esa hoja.")

        elif opcion == "4":
            hoja = elegir_hoja(datos_preparados)
            if hoja:
                tabla = datos_preparados[hoja]
                if tabla is not None and not es_hoja_medidas(hoja):
                    ejecutar_grafica_especifica(hoja, tabla)
                else:
                    print("La hoja de medidas no usa este menú de gráficas.")

        elif opcion == "5":
            hoja = elegir_hoja(datos_preparados)
            if hoja:
                tabla = datos_preparados[hoja]
                if tabla is not None and not es_hoja_medidas(hoja):
                    generar_todas_las_graficas(hoja, tabla)
                else:
                    print("La hoja de medidas no usa este menú de gráficas.")

        elif opcion == "6":
            if "Medidas" in datos_preparados:
                mostrar_hoja_medidas(datos_preparados["Medidas"])
            else:
                print("No se encontró la hoja 'Medidas'.")

        elif opcion == "7":
            generar_todo_el_proyecto(datos_preparados)

        elif opcion == "8":
            print("\nPrograma finalizado. ¡Proyecto terminado!")
            break

        else:
            print("Opción inválida. Inténtalo otra vez.")


