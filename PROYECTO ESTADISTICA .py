    plt.bar(x, y, color="firebrick")
    plt.title(f"Barras - {titulo}")
    plt.xlabel("Categoría / Valor")
    plt.ylabel("Frecuencia")
    plt.xticks(rotation=30, ha="right")
    plt.tight_layout()
    plt.show()

