def detectar_tipo_hoja(tabla):
    """
    Determina si la hoja es numérica o categórica.
    """
    if tabla is None or tabla.empty:
        return "desconocida"

    primera_col = tabla.columns[0]
    muestra = pd.to_numeric(tabla[primera_col], errors="coerce")

    # Si la mayoría de la primera columna es numérica, es hoja numérica
    proporcion_numerica = muestra.notna().mean()
    if proporcion_numerica >= 0.6:
        return "numerica"
    return "categorica"


def obtener_columnas_principales(tabla):
    """
    Busca columnas esperadas:
    x/respuesta, frecuencia, frecuencia relativa, acumuladas
    """
    columnas = list(tabla.columns)

    col_base = columnas[0]
    col_frecuencia = None
    col_relativa = None
    col_acum_menor = None
    col_acum_mayor = None

    for c in columnas:
        texto = str(c).lower()
        if "frecuencia" in texto and "relativa" not in texto and "acum" not in texto:
            col_frecuencia = c
        elif "frecuencia relativa" in texto:
            col_relativa = c
        elif "acum" in texto and "menor" in texto:
            col_acum_menor = c
        elif "acum" in texto and "mayor" in texto:
            col_acum_mayor = c

    return {
        "base": col_base,
        "frecuencia": col_frecuencia,
        "relativa": col_relativa,
        "acum_menor": col_acum_menor,
        "acum_mayor": col_acum_mayor
    }


