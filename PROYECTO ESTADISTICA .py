def normalizar_nombre_columna(col):
    if pd.isna(col):
        return "columna_vacia"
    return str(col).strip()

def limpiar_dataframe(df):
    df = df.copy()
    df.columns = [normalizar_nombre_columna(c) for c in df.columns]
    df = df.dropna(how="all")
    df = df.dropna(axis=1, how="all")
    df = df.reset_index(drop=True)
    return df

def obtener_hojas_utiles(libro):
    return [h for h in libro.keys() if h.lower() not in ["portada"]]

def es_hoja_medidas(nombre_hoja):
    return nombre_hoja.strip().lower() == "medidas"

def convertir_si_numero(serie):
    return pd.to_numeric(serie, errors="coerce")

def detectar_tabla_analizable(df):
    """
    Intenta detectar la fila donde empiezan encabezados útiles.
    En tus hojas suele ser la fila 1.
    """
    df = limpiar_dataframe(df)

    if len(df) < 2:
        return None

    # Intento 1: usar la segunda fila como encabezado real
    posible = df.copy()
    encabezados = [str(x).strip() for x in posible.iloc[0].tolist()]
    # si la primera fila es muy vacía, usamos la segunda
    if sum(pd.isna(posible.iloc[0])) > sum(pd.isna(posible.iloc[1])) and len(posible) > 1:
        encabezados = [str(x).strip() for x in posible.iloc[1].tolist()]
        posible = posible.iloc[2:].copy()
    else:
        posible = posible.iloc[1:].copy()

    posible.columns = encabezados
    posible = posible.dropna(how="all")
    posible = posible.reset_index(drop=True)

    return posible

def preparar_hoja(nombre_hoja, df):
    """
    Devuelve un DataFrame limpio y útil para análisis.
    """
    if es_hoja_medidas(nombre_hoja):
        return limpiar_dataframe(df)

    tabla = detectar_tabla_analizable(df)
    if tabla is None or tabla.empty:
        return None

    # quitar fila TOTAL si existe, pero guardarla opcionalmente
    tabla = tabla.copy()

    # Normalizar primera columna
    primera_col = tabla.columns[0]
    tabla[primera_col] = tabla[primera_col].astype(str).str.strip()

    # eliminar filas vacías
    tabla = tabla[tabla[primera_col].str.lower() != "nan"]

    return tabla.reset_index(drop=True)


